import Foundation

// variable

// implicit declaration

// Int
var num = 100

// console.log(`num = ${num}`)
print("num = \(num)")

// console.log(`type of num = ${typeof(num)}`)
print("type of num = \(type(of: num))")

// String
var name = "steve"
print("name = \(name), type = \(type(of: name))")

// Double
var salary = 10.50
print("salary = \(salary), type = \(type(of: salary))")

// Bool
var canVote = true
print("canVote = \(canVote), type = \(type(of: canVote))")
